package com.javalec.variable_2;

public class variable_2 {
	public static void main(String[] args) {

		//변수와 상수
		String str = "James";
		int num = 10;
		double numDouble = 20.2;
		char chr = 'a';
		boolean bool = true;
				
		System.out.println(str + " : " + num);
		System.out.println(num + num);
		System.out.println( (double)num + numDouble );
		System.out.println( num + (int)numDouble );
		System.out.println(chr);
		System.out.println(bool);
		
	}
}

